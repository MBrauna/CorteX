class geraLog
{

    constructor()
    {
        // MÃ©todo construtor para geraLog
        this.v_tipo_log = 1; // [1] - Console, [2] - DB, [3] - Console e DB
    } // constructor()



    gera_log(p_mensagem)
    {
        this.gera_log_console(p_mensagem);
    } // gera_log(p_mensagem)



    gera_log_console(p_mensagem)
    {
        console.log('[LOG] ' + p_mensagem);
    } // gera_log_console(p_mensagem)
} // class geraLog


module.exports = geraLog;